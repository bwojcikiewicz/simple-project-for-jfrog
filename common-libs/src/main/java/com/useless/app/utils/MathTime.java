package com.useless.app.utils;

public class MathTime {

    public static int add(int val1, int val2){
        return val1 + val2;
    }

    public static int subs(int val1, int val2){
        return val1 - val2;
    }

    public static int power(int val1, int val2){
        return val1 * val2;
    }
}
